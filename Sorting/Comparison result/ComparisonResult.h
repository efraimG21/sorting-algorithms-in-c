#ifndef ComparisonResult_h
#define ComparisonResult_h

// Enumeration for comparison results
typedef enum {
    LESS_THAN = -1, // Value indicating the first item is less than the second
    EQUAL,         // Value indicating both items are equal
    GREATER_THAN   // Value indicating the first item is greater than the second
} ComparisonResult;

#endif /* ComparisonResult_h */
